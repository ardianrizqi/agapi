        //
        $posts = Post::where('author_id', $userId)
            ->with([
                'images',
                'author.profile',
                'author.role',
                'comments.user',
                'likes.user',
            ])
            ->has('images')
            ->orderBy('created_at', 'desc')
            ->paginate();
