     * @return \Illuminate\Http\Response
     */
    public function index($id)
    {
        $user = User::findOrFail($id);
        $documents = \App\Models\File::with('fileable')->whereHasMorph('fileable','App\Models\Post',function($query)use($user){
            $query->where('author_id','=',$user->id);
        })->where('value','document')->orderBy('id','desc');
        return ['user'=>$user,'documents'=>$documents->paginate()];
    }

