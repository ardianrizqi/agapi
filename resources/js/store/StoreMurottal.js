const MurottalMutations = {
    playMurottal(state, id) {
        state.murottals.forEach((val, key) => {
            val.isPlaying = false;
            if (val.id == id) {
                if (!val.audio.paused) {
                    val.isPlaying = false;
                    val.audio.pause();
                } else {
                    state.activeMurottal = key;
                    val.isPlaying = true;
                    val.audio.play();
                }
            } else {
                val.audio.isPlaying = false;
                val.audio.pause();
                val.audio.currentTime = 0;
            }
        });
    }
};

